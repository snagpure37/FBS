
public class ComplexNumber {
	private int real;
	private int imaginary;
	
	public ComplexNumber() 
	{
		
	}
	
	public ComplexNumber(int real, int img)
	{
		this.real = real;
		this.imaginary = img;
	}
	
	public int getReal()
	{
		return real;
	}
	
	public int getImaginary()
	{
		return imaginary;
	}
	

}
